void SpeakerSequencer::setup()
{
  pinMode(SPEAKER_PIN, OUTPUT);
  curnoteidx = 0;
  songptr = zelda_melody;
  tempo = 88;
  totalnotes =sizeof(zelda_melody)/sizeof(zelda_melody[0])/2; 
  wholenote = (60000 * 4) / tempo;
  nextnote = 0;
}

void SpeakerSequencer::update()
{
  // regarde le temps ecouler et set le tone selon la commande
  // avance a la prochaine commande
  if ( playing )
  {
    if ( millis() >= nextnote)
    {
      int divider = 0, noteDuration = 0;
      //divider = songptr[curnoteidx + 1];
      divider = pgm_read_word_near(songptr+curnoteidx + 1);
      if (divider > 0) {
        // regular note, just proceed
        noteDuration = (wholenote) / divider;
      } else if (divider < 0) {
        // dotted notes are represented with negative durations!!
        noteDuration = (wholenote) / abs(divider);
        noteDuration *= 1.5; // increases the duration in half for dotted notes
      }
  
      // we only play the note for 90% of the duration, leaving 10% as a pause
      //tone(SPEAKER_PIN, songptr[curnoteidx], noteDuration*0.9);
      tone(SPEAKER_PIN, pgm_read_word_near(songptr+curnoteidx), noteDuration*0.9);
  
      // Wait for the specief duration before playing the next note.
      nextnote = millis() + noteDuration;
      curnoteidx += 2;
      if (curnoteidx >= totalnotes)
      {
        curnoteidx = 0;
      } 
    }
  }
}


void SpeakerSequencer::Start()
{
  playing = true;
  curnoteidx = 0;
  nextnote = 0;
}

void SpeakerSequencer::Stop()
{
  playing = false;
  noTone(SPEAKER_PIN);
}

